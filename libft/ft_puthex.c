#include <unistd.h>

void	ft_puthex(int num)
{
	char			c;

	c = '0';
	if (num > 15)
	{
		ft_puthex(num / 16);
		num = num % 16;
	}
	while (num > 0)
	{
		num--;
		c++;
		if (c == ':')
			c = 'a';
	}
	write(1, &c, 1);
}
