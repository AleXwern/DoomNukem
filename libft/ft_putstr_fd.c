#include "libft.h"

void	ft_putstr_fd(const char *s, int fd)
{
	char			c;
	unsigned int	i;

	i = 0;
	while (s[i] != '\0')
	{
		c = s[i];
		write(fd, &c, 1);
		i++;
	}
}
